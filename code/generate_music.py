def extract_and_convert(zip_path="MusicXML_Files.zip", out="midi_files"):
    os.makedirs(out, exist_ok=True)
    os.makedirs("musicxml_extracted", exist_ok=True)
    if not os.path.exists(zip_path): return 0

    with zipfile.ZipFile(zip_path,'r') as z:
        z.extractall("musicxml_extracted")

    xmls = []
    for ext in ['*.xml','*.mxl','*.musicxml']:
        xmls += [f for f in Path("musicxml_extracted").rglob(ext) if not f.name.startswith('._')]

    cnt = 0
    for x in tqdm(xmls, desc="Converting MusicXML to MIDI"):
        try:
            s = converter.parse(str(x))
            mf = m21midi.translate.music21ObjectToMidiFile(s)
            mf.open(os.path.join(out, x.stem+".mid"), 'wb')
            mf.write()
            mf.close()
            cnt += 1
        except: continue
    return cnt


